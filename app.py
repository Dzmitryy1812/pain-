st.divider()
st.subheader("🧠 Расшифровка ситуации для Polymarket")

analysis_cols = st.columns(3)

# 1. Анализ Магнита (Max Pain)
with analysis_cols[0]:
    st.write("**🧲 Влияние 'Магнита':**")
    if abs(price - max_pain) < 500:
        st.success("Цена на точке Max Pain. Это самая стабильная зона. Идеально для удержания.")
    elif price > max_pain:
        st.info(f"Цена выше Max Pain на {((price/max_pain-1)*100):.1f}%. Ожидается давление ПРОДАВЦОВ к уровню ${max_pain:,.0f}. Это ХОРОШО для позиции NO {p_high}.")
    else:
        st.info(f"Цена ниже Max Pain на {((max_pain/price-1)*100):.1f}%. Ожидается давление ПОКУПАТЕЛЕЙ к уровню ${max_pain:,.0f}. Это ХОРОШО для позиции YES {p_low}.")

# 2. Анализ Гаммы (Волатильность)
with analysis_cols[1]:
    st.write("**📊 Устойчивость рынка:**")
    # Находим GEX для ближайшего страйка
    current_strike_idx = (np.abs(strikes - price)).argmin()
    current_gex = gex_vals[current_strike_idx]
    
    if current_gex > 0:
        st.success("ПОЛОЖИТЕЛЬНАЯ ГАММА. Маркетмейкеры гасят движения. Рынок 'вязкий', резких скачков не ожидается. Хорошо для коридора.")
    else:
        st.error("ОТРИЦАТЕЛЬНАЯ ГАММА. Зона высокой турбулентности! Возможны резкие 'прострелы' цены. Ваша позиция NO под угрозой.")

# 3. Вердикт по времени (Theta)
with analysis_cols[2]:
    st.write("**⏳ Фактор времени:**")
    if hours_left > 48:
        st.write("Времени еще много. Цена может гулять. Не спешите радоваться прибыли.")
    elif 12 < hours_left <= 48:
        st.warning("ЗОЛОТОЕ ОКНО. Время сгорает быстро. Если цена далеко от границ — ваша прибыль растет ежечасно.")
    else:
        st.error("ФИНАЛЬНЫЙ ГАМБИТ. Риск максимален. Если цена близко к 76k, лучше закрыть позицию и не играть в лотерею.")

# ОБЩИЙ ВЫВОД
st.info("💡 **ИТОГОВЫЙ СОВЕТ:**")
if price < (p_high * 0.97) and current_gex > 0 and price > (p_low * 1.03):
    st.write("✅ Все системы в норме. Цена в защищенном коридоре. Вероятность закрытия в плюс высокая.")
elif price >= (p_high * 0.98):
    st.write("🚨 **ВНИМАНИЕ!** Цена опасно близко к верхней границе (76k). Опционный барьер может не выдержать. Рассмотрите выход или хедж.")
elif current_gex < 0:
    st.write("⚠️ **ОСТОРОЖНО:** Рынок нестабилен (Negative Gamma). Даже если цена сейчас далеко, она может долететь до границ за один час.")
else:
    st.write("🧐 Ситуация нейтральная. Следите за тем, не начнет ли Max Pain ползти вверх.")
